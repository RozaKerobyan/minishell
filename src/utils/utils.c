#include "minishell.h"

void	close_fd(int *fd)
{
	if (!fd || *fd == -1)
	{
		return ;
	}
	close(*fd);
	*fd = -1;
}

int	args_len(char **args)
{
	int	i;

	i = 0;
	while (args[i])
		i++;
	return (i);
}
